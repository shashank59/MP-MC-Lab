.MODEL SMALL

.DATA
N DB 5
A DB 05H,07H,04H,03H,06H

.CODE
MOV AX,@DATA
MOV DS,AX
MOV CL,N
DEC CL
OUTLOOP:
MOV CH,CL
MOV SI,00H
INLOOP:
MOV AL,A[SI]
INC SI
CMP AL,A[SI]
JC NOEXCH
XCHG AL,A[SI]
MOV A[SI-1],AL
NOEXCH:
DEC CH
JNZ INLOOP
DEC CL
JNZ OUTLOOP
MOV AH,4CH
INT 21H
END
